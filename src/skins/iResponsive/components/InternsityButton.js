const intensityImages = {};

intensityImages[SPECIAL_EFFECTS_INTENSITY.LOW] = intensityLowImage;
intensityImages[SPECIAL_EFFECTS_INTENSITY.MEDIUM] = intensityMediumImage;
intensityImages[SPECIAL_EFFECTS_INTENSITY.HIGH] = intensityHighImage;

export default class IntensityButton {
	constructor(parentElement, opts={}) {
		this.intensity  = SPECIAL_EFFECTS_INTENSITY.LOW;
		this.onIntensityChanged = opts.onIntensityChanged || (() => {});

		this.button = document.createElement('button');
		this.button.id = 'btn-intensity';
		this.button.className = 'intensity-button';
		parentElement.appendChild(this.button);

		this.button.onclick = () => {
			switch (this.intensity) {
				case SPECIAL_EFFECTS_INTENSITY.LOW:
					this.intensity = SPECIAL_EFFECTS_INTENSITY.MEDIUM;
					break;
				case SPECIAL_EFFECTS_INTENSITY.MEDIUM:
					this.intensity = SPECIAL_EFFECTS_INTENSITY.HIGH;
					break;
				case SPECIAL_EFFECTS_INTENSITY.HIGH:
					this.intensity = SPECIAL_EFFECTS_INTENSITY.LOW;
					break;
			}

			this.render();
			this.onIntensityChanged(this.intensity);
		};

		this.render();

	}
	render() {
		console.log('render intensity button');
		this.button.innerHTML = intensityImages[this.intensity];
	}

	setIntensity(intensity) {
		this.intensity = intensity;
		this.render();

	}
}

